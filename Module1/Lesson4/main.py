my_string = input("Введите любой текст: ")
print("Длина введенной строки:", len(my_string))
print("Cтрока в верхнем регистре:", my_string.upper())
print("Cтрока в нижнем регистре:", my_string.lower())
print("Cтрока без пробелов:", my_string.replace(" ", ""))
print("Первый символ строки:", my_string[0])
print("Последний символ строки:", my_string[-1])

